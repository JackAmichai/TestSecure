def _add(topic, question, options, answer, explanation):
    """Append a question after sanity-checking the schema."""
    assert len(options) == 4, "every question must have exactly 4 options"
    assert answer in options, f"answer not in options: {answer}"
    assert len(explanation) >= 80, f"explanation too short for: {question}"
    BONUS.append({
        "topic": topic,
        "question": question,
        "options": list(options),
        "answer": answer,
        "explanation": explanation,
    })


